import Foundation

public enum Color {
    public static let white = UIColor.white
    public static let red = UIColor.red
    public static let lightRed = UIColor(red: 255, green: 0, blue: 0, alpha: 0.5)
    public static let light = UIColor(red: 153/255, green: 153/255, blue: 1, alpha: 1)
    public static let dark = UIColor(red: 0, green: 61/255, blue: 153/255, alpha: 1)
    public static let lightGray = UIColor(red: 125/255, green: 125/255, blue: 125/255, alpha: 1)
    public static let darkGray = UIColor(red: 80/255, green: 80/255, blue: 80/255, alpha: 1)
}
